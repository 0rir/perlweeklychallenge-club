#[test]
fn test_ex1() {
    assert_eq!(longyear(),vec![1903, 1908, 1914, 1920, 1925,
                               1931, 1936, 1942, 1948, 1953,
                               1959, 1964, 1970, 1976, 1981,
                               1987, 1992, 1998, 2004, 2009,
                               2015, 2020, 2026, 2032, 2037,
                               2043, 2048, 2054, 2060, 2065,
                               2071, 2076, 2082, 2088, 2093,
                               2099]);
}

fn p(y: i32) -> i32 {
    (y+y/4-y/100+y/400) % 7
}

fn longyear() -> Vec<i32> {
    (1900..=2100).into_iter().filter(|y| p(*y-1)==3 || p(*y)==4).collect()
}
